func TestLoadConfig_ParsesFullConfig(t *testing.T) {
	dir := t.TempDir()
	writeFile(t, dir, ".fmp.yaml", `paths:
  - clusters/production
  - clusters/staging
recursive: true
helm: false
resolve-git: true
sops-decrypt: false
sort: true
exclude-crds: true
filters:
  - kind: FieldNormalizer
    match:
      kind: Secret
    fieldPaths:
      - path: [data, "tls.*"]
        action: replace
        placeholder: "<<auto-generated>>"
helm-settings:
  repository-cache: /tmp/helm-cache
`)

	cfg, err := LoadConfig(dir)
	if err != nil {
		t.Fatalf("LoadConfig() error = %v", err)
	}
	if cfg == nil {
		t.Fatal("expected config, got nil")
	}

	if len(cfg.Paths) != 2 {
		t.Errorf("expected 2 paths, got %d", len(cfg.Paths))
	}
	if !BoolOr(cfg.Recursive, false) {
		t.Error("expected recursive=true")
	}
	if BoolOr(cfg.Helm, true) {
		t.Error("expected helm=false")
	}
	if !BoolOr(cfg.ResolveGit, false) {
		t.Error("expected resolve-git=true")
	}
	if BoolOr(cfg.SOPSDecrypt, false) {
		t.Error("expected sops-decrypt=false")
	}
	if !BoolOr(cfg.Sort, false) {
		t.Error("expected sort=true")
	}
	if !BoolOr(cfg.ExcludeCRDs, false) {
		t.Error("expected exclude-crds=true")
	}
	if len(cfg.Filters.Filters) != 1 {
		t.Fatalf("expected 1 filter, got %d", len(cfg.Filters.Filters))
	}
	if cfg.HelmSettings == nil || cfg.HelmSettings.RepositoryCache != "/tmp/helm-cache" {
		t.Errorf("expected helm-settings.repository-cache=/tmp/helm-cache, got %v", cfg.HelmSettings)
	}
}

