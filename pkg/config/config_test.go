func TestBoolOr(t *testing.T) {
	if BoolOr(nil, true) != true {
		t.Error("expected default true when nil")
	}
	if BoolOr(nil, false) != false {
		t.Error("expected default false when nil")
	}
	if BoolOr(BoolPtr(true), false) != true {
		t.Error("expected true from pointer, ignoring default")
	}
	if BoolOr(BoolPtr(false), true) != false {
		t.Error("expected false from pointer, ignoring default")
	}
}

